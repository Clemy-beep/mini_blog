let link1 = document.getElementById("menuitem1");

link1.addEventListener("mouseenter", function(event) {
    event.target.style.color = '#E1A560';
}, false);

link1.addEventListener("mouseleave", function(event) {
    event.target.style.color = "";
}, false);

